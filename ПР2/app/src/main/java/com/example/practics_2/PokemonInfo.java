package com.example.practics_2;

public class PokemonInfo {
    public String name;
    public String url;
    public int height;
    public int weight;

    public PokemonInfo(String name, String url, int height, int weight) {
        this.name = name;
        this.url = url;
        this.height = height;
        this.weight = weight;
    }
}